function jobfind_category_table_exists($conn, $table)
{
    if (!$conn || !preg_match('/^[A-Za-z0-9_]+$/', $table)) {
        return false;
    }

    $table_sql = mysqli_real_escape_string($conn, $table);
    $result = mysqli_query($conn, "SHOW TABLES LIKE '$table_sql'");
    return $result && mysqli_num_rows($result) > 0;
}

function jobfind_category_column_exists($conn, $table, $column)
{
    if (!$conn || !preg_match('/^[A-Za-z0-9_]+$/', $table) || !preg_match('/^[A-Za-z0-9_]+$/', $column)) {
        return false;
    }

    $column_sql = mysqli_real_escape_string($conn, $column);
    $result = mysqli_query($conn, "SHOW COLUMNS FROM `$table` LIKE '$column_sql'");
    return $result && mysqli_num_rows($result) > 0;
}

function jobfind_category_add_column_if_missing($conn, $table, $column, $definition)
{
    if (!jobfind_category_table_exists($conn, $table) || jobfind_category_column_exists($conn, $table, $column)) {
        return true;
    }

    return (bool)mysqli_query($conn, "ALTER TABLE `$table` ADD COLUMN `$column` $definition");
}

function ensure_category_schema($conn)
{
    if (!$conn) {
        return false;
    }

    mysqli_query($conn, "
        CREATE TABLE IF NOT EXISTS categories (
            category_id INT AUTO_INCREMENT PRIMARY KEY,
            name VARCHAR(100) NOT NULL,
            icon VARCHAR(20) DEFAULT '📦',
            description TEXT DEFAULT NULL,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci
    ");

    mysqli_query($conn, "
        CREATE TABLE IF NOT EXISTS job_subcategories (
            subcategory_id INT AUTO_INCREMENT PRIMARY KEY,
            category_id INT NOT NULL,
            name VARCHAR(120) NOT NULL,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            UNIQUE KEY uq_job_subcategory (category_id, name),
            INDEX idx_job_subcategory_category (category_id)
        ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci
    ");

    mysqli_query($conn, "
        CREATE TABLE IF NOT EXISTS category_seed_runs (
            seed_key VARCHAR(100) NOT NULL PRIMARY KEY,
            applied_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
        ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci
    ");

    jobfind_category_add_column_if_missing($conn, 'job', 'job_subcategory', 'VARCHAR(120) DEFAULT NULL AFTER `category`');

    return true;
}

function ensure_default_job_categories($conn)
{
    if (!$conn) {
        return false;
    }

    ensure_category_schema($conn);
    jobfind_migrate_legacy_categories($conn);

    foreach (jobfind_default_job_category_groups() as $category) {
        $name_sql = mysqli_real_escape_string($conn, $category['name']);
        $icon_sql = mysqli_real_escape_string($conn, $category['icon']);
        $category_row = mysqli_fetch_assoc(mysqli_query($conn, "SELECT category_id FROM categories WHERE name='$name_sql' LIMIT 1"));

        if ($category_row) {
            $category_id = (int)$category_row['category_id'];
        } else {
            mysqli_query($conn, "INSERT INTO categories (name, icon) VALUES ('$name_sql', '$icon_sql')");
            $category_id = (int)mysqli_insert_id($conn);
        }

        foreach ($category['subcategories'] as $subcategory) {
            $subcategory_sql = mysqli_real_escape_string($conn, $subcategory);
            mysqli_query($conn, "
                INSERT IGNORE INTO job_subcategories (category_id, name)
                VALUES ('$category_id', '$subcategory_sql')
            ");
        }
    }

    $seed_key_sql = mysqli_real_escape_string($conn, 'job_categories_subcategories_thai_v1');
    mysqli_query($conn, "INSERT IGNORE INTO category_seed_runs (seed_key) VALUES ('$seed_key_sql')");
    return true;
}

function jobfind_migrate_legacy_categories($conn)
{
    $legacy_categories = [
        'IT' => ['name' => 'เทคโนโลยีและซอฟต์แวร์', 'icon' => '💻'],
        'IT & Software' => ['name' => 'เทคโนโลยีและซอฟต์แวร์', 'icon' => '💻'],
